def download_repos_py_files(
    repos: List[str],
    max_files_per_repo: int = 200,
    token: Optional[str] = None,
    on_progress: Optional[callable] = None,
) -> Iterator[Tuple[str, GitHubFile, str]]:
    """Download Python files from multiple repos.

    Yields (repo, GitHubFile, content) tuples as files are downloaded.

    Example:
        for repo, gh_file, content in download_repos_py_files(
            ["django/django", "psf/requests"],
            max_files_per_repo=50,
        ):
            print(f"{repo}/{gh_file.path}: {len(content)} bytes")
    """
    client = GitHubClient(token=token)

    for repo in repos:
        owner, repo_name = repo.split("/", 1)
        print(f"[GitHub] Fetching {repo}...")

        try:
            file_count = 0
            for gh_file in client.list_py_files(repo, max_files=max_files_per_repo):
                content = client.download_file(gh_file)
                if content is not None:
                    # Basic validation — must be valid-ish Python
                    if _looks_like_python(content):
                        file_count += 1
                        if on_progress:
                            on_progress(repo, gh_file.path, file_count)
                        yield repo, gh_file, content
        except Exception as e:
            print(f"[GitHub] Error fetching {repo}: {e}")


def _looks_like_python(content: str) -> bool:
    """Heuristic: does this look like Python source code?"""
    # Skip very short or binary-looking content
    if len(content) < 10:
        return False
    # Skip files that are clearly not Python
    non_python_indicators = ["<?xml", "<!DOCTYPE", "<?php", "<html", "package-lock.json"]
    for indicator in non_python_indicators:
        if content.strip().startswith(indicator):
            return False
    return True


def save_cache(cached: CachedFiles, path: Path) -> None:
    """Save a cache of downloaded files to a JSON file."""
    data = {
        "repo": cached.repo,
        "saved_at": datetime.now(timezone.utc).isoformat(),
        "files": [
            {"path": gh_file.path, "sha": gh_file.sha, "content": content}
            for gh_file, content in cached.files
        ],
    }
    with open(path, "w", encoding="utf-8") as f:
        json.dump(data, f, indent=2)


def load_cache(path: Path) -> Optional[CachedFiles]:
    """Load a cached file list from a JSON file."""
    if not path.exists():
        return None
    try:
        with open(path, "r", encoding="utf-8") as f:
            data = json.load(f)
        files = []
        for item in data.get("files", []):
            gh_file = GitHubFile(
                repo=data.get("repo", ""),
                path=item["path"],
                sha=item["sha"],
                size=len(item["content"]),
                url="",
                raw_url="",
            )
            files.append((gh_file, item["content"]))
        return CachedFiles(files=files, repo=data.get("repo", ""), saved_at=data.get("saved_at", ""))
    except Exception:
        return None