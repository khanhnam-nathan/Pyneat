#!/bin/bash
# PyNEAT Plugin Installation Script
# Installs PyNEAT plugins for all supported IDEs

set -e

echo "=========================================="
echo "PyNEAT Plugin Installation"
echo "=========================================="
echo ""

# Detect OS
OS="$(uname -s)"
case "$OS" in
    Linux*)     OS_NAME="linux";;
    Darwin*)    OS_NAME="macos";;
    CYGWIN*)    OS_NAME="windows";;
    MINGW*)     OS_NAME="windows";;
    MSYS*)      OS_NAME="windows";;
    *)          OS_NAME="unknown";;
esac

echo "Detected OS: $OS_NAME"

# Get script directory
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
PYNEAT_ROOT="$(dirname "$SCRIPT_DIR")"

echo "PyNEAT root: $PYNEAT_ROOT"
echo ""

# Function to install Neovim plugin
install_neovim() {
    echo "Installing Neovim plugin..."

    if [ "$OS_NAME" = "windows" ]; then
        NVIM_CONFIG_DIR="${HOME}/AppData/Local/nvim"
    else
        NVIM_CONFIG_DIR="${HOME}/.config/nvim"
    fi

    mkdir -p "$NVIM_CONFIG_DIR/lua/pyneat"

    # Copy plugin files
    cp "$PYNEAT_ROOT/vim-plugin/lua/pyneat.lua" "$NVIM_CONFIG_DIR/lua/pyneat/"
    cp "$PYNEAT_ROOT/vim-plugin/lua/pyneat-lsp.lua" "$NVIM_CONFIG_DIR/lua/pyneat/"

    echo "  -> Copied to $NVIM_CONFIG_DIR/lua/pyneat/"

    # Create plugin entry point
    mkdir -p "$NVIM_CONFIG_DIR/plugin"
    cat > "$NVIM_CONFIG_DIR/plugin/pyneat.vim" << 'VIMEOF'
" PyNEAT Plugin for Neovim
" Auto-generated by install-plugins.sh

lua << EOF
require('pyneat').setup({
    enable_security = true,
    enable_ai_bugs = true,
    auto_clean_on_save = false,
})
EOF

" Commands
command! PyneatCheck lua require('pyneat').check()
command! PyneatClean lua require('pyneat').clean()
command! PyneatManifest lua require('pyneat').manifest()
VIMEOF

    echo "  -> Created Neovim plugin entry: $NVIM_CONFIG_DIR/plugin/pyneat.vim"
    echo "Neovim plugin installed!"
}

# Function to install VS Code extension
install_vscode() {
    echo ""
    echo "VS Code Extension:"
    echo "  -> The VS Code extension is located at: $PYNEAT_ROOT/vscode-extension/"
    echo "  -> To install:"
    echo "     1. cd $PYNEAT_ROOT/vscode-extension"
    echo "     2. npm install"
    echo "     3. npm run package"
    echo "     4. Open VS Code and run: Extensions: Install from VSIX"
    echo ""
    echo "  -> Or use VS Code command:"
    echo "     code --install-extension pyneat-vscode-*.vsix"
}

# Function to install JetBrains plugin
install_jetbrains() {
    echo ""
    echo "JetBrains Plugin:"
    echo "  -> The JetBrains plugin is located at: $PYNEAT_ROOT/jetbrains-plugin/"
    echo "  -> To build:"
    echo "     1. cd $PYNEAT_ROOT/jetbrains-plugin"
    echo "     2. ./gradlew build"
    echo "     3. Install the .jar from build/libs/"
    echo ""
    echo "  -> Or install from JetBrains Marketplace (after publishing):"
    echo "     Settings -> Plugins -> Search 'PyNEAT' -> Install"
}

# Function to install Vim plugin
install_vim() {
    echo ""
    echo "Vim Plugin (basic):"
    echo "  -> For Vim, use the Neovim Lua plugin or manually add:"
    echo ""
    echo "     Plugin 'pyneat/pyneat-vim'"
    echo ""
    echo "  -> Or copy the plugin files manually to your vim plugin directory."
}

# Function to verify PyNEAT installation
verify_pyneat() {
    echo ""
    echo "Verifying PyNEAT installation..."

    if command -v python &> /dev/null; then
        PYTHON_CMD="python"
    elif command -v python3 &> /dev/null; then
        PYTHON_CMD="python3"
    else
        echo "  [WARNING] Python not found in PATH"
        return 1
    fi

    if $PYTHON_CMD -m pyneat.cli --version &> /dev/null; then
        VERSION=$($PYTHON_CMD -m pyneat.cli --version)
        echo "  [OK] PyNEAT is installed: $VERSION"
    else
        echo "  [WARNING] PyNEAT not found. Install with:"
        echo "     pip install pyneat-cli"
    fi
}

# Main menu
show_help() {
    echo "Usage: $0 [OPTIONS]"
    echo ""
    echo "Options:"
    echo "  --all         Install all plugins (default)"
    echo "  --neovim      Install Neovim plugin"
    echo "  --vscode      Show VS Code installation instructions"
    echo "  --jetbrains   Show JetBrains installation instructions"
    echo "  --vim         Show Vim installation instructions"
    echo "  --verify      Verify PyNEAT installation"
    echo "  --help        Show this help message"
    echo ""
}

# Parse arguments
INSTALL_ALL=true
INSTALL_NEOVIM=false
SHOW_VSCODE=false
SHOW_JETBRAINS=false
SHOW_VIM=false
VERIFY_ONLY=false

while [[ $# -gt 0 ]]; do
    case $1 in
        --all)
            INSTALL_ALL=true
            shift
            ;;
        --neovim)
            INSTALL_NEOVIM=true
            INSTALL_ALL=false
            shift
            ;;
        --vscode)
            SHOW_VSCODE=true
            INSTALL_ALL=false
            shift
            ;;
        --jetbrains)
            SHOW_JETBRAINS=true
            INSTALL_ALL=false
            shift
            ;;
        --vim)
            SHOW_VIM=true
            INSTALL_ALL=false
            shift
            ;;
        --verify)
            VERIFY_ONLY=true
            INSTALL_ALL=false
            shift
            ;;
        --help)
            show_help
            exit 0
            ;;
        *)
            echo "Unknown option: $1"
            show_help
            exit 1
            ;;
    esac
done

# Run installations
if [ "$VERIFY_ONLY" = true ]; then
    verify_pyneat
elif [ "$INSTALL_ALL" = true ]; then
    verify_pyneat
    install_neovim
    install_vscode
    install_jetbrains
    install_vim
    echo ""
    echo "=========================================="
    echo "Installation complete!"
    echo "=========================================="
elif [ "$INSTALL_NEOVIM" = true ]; then
    install_neovim
elif [ "$SHOW_VSCODE" = true ]; then
    install_vscode
elif [ "$SHOW_JETBRAINS" = true ]; then
    install_jetbrains
elif [ "$SHOW_VIM" = true ]; then
    install_vim
fi
